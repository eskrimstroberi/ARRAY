package tugas_8_array_meivayusnita;
public class TUGAS_1_ARRAY_MEIVAYUSNITA {
    public static void main(String[] args) {
    String nama [][]={{"alya","18765"},
                      {"aladin","11876"},
                      {"nala","17622"},
                      {"wawan","28899"},
                      {"setya","98765"}};
    for (int i=0; i<5; i++){
        for(int h=0; h<2; h++){
            System.out.println(nama[i][h]+" ");
        }
        System.out.println(" ");
    }
    }
}

