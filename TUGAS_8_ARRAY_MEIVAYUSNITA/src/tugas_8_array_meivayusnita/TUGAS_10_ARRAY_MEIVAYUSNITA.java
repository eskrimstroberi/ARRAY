package tugas_8_array_meivayusnita;
public class TUGAS_10_ARRAY_MEIVAYUSNITA {
    public static void main(String[] args) {
         String[][] nama =  {
                {"Arya","10001"},
                {"bagas","10002"},
                {"Chintya","10003"},
              };
            for (int x=0; x<nama.length; x++){
                System.out.println("nama: " +nama[x][0]);
                System.out.println("nama: " +nama[x][1]);
                System.out.println("=============");
            }
        } 
}
